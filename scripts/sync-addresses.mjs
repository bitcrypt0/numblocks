import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

/**
 * Regenerates lib/web3/addresses.ts from ../deployments/mainnet/*.json so the
 * frontend never hand-maintains contract addresses. Run after any redeploy:
 *   pnpm sync:addresses
 */
const here = dirname(fileURLToPath(import.meta.url));
const deployments = join(here, "..", "..", "deployments", "mainnet");

const FILES = {
  core: "NumberBlocksCore.json",
  uniBlocks: "UniBlocks.json",
  blocksSale: "BlocksSale.json",
  hook: "NumberBlocksHook.json",
  swapHelper: "SwapHelper.json",
  lpLocker: "NumberBlocksV4LPPositionLocker.json",
  renderer: "Renderer.json",
  metadata: "Metadata.json",
};

const entries = Object.entries(FILES).map(([key, file]) => {
  const json = JSON.parse(readFileSync(join(deployments, file), "utf8"));
  if (!/^0x[0-9a-fA-F]{40}$/.test(json.address ?? "")) {
    throw new Error(`${file}: missing or malformed "address"`);
  }
  return [key, json.address];
});

const swapHelper = JSON.parse(readFileSync(join(deployments, FILES.swapHelper), "utf8"));
const hookJson = JSON.parse(readFileSync(join(deployments, FILES.hook), "utf8"));

const body = `// AUTO-GENERATED by scripts/sync-addresses.mjs from deployments/mainnet/*.json.
// Do not edit by hand - rerun \`pnpm sync:addresses\` after a redeploy.
import type { Address } from "viem";

export const MAINNET_ADDRESSES = {
${entries.map(([k, a]) => `  ${k}: "${a}" as Address,`).join("\n")}
} as const;

export type ContractName = keyof typeof MAINNET_ADDRESSES;

/** Canonical Uniswap v4 PoolManager on mainnet (from the hook deploy record). */
export const POOL_MANAGER = "${hookJson.poolManager}" as Address;

/** Official v4 PoolKey recorded at deploy (currency0 = native ETH). */
export const OFFICIAL_POOL_KEY = {
  currency0: "0x0000000000000000000000000000000000000000" as Address,
  currency1: MAINNET_ADDRESSES.uniBlocks,
  fee: ${swapHelper.fee},
  tickSpacing: ${swapHelper.tickSpacing},
  hooks: MAINNET_ADDRESSES.hook,
} as const;
`;

const out = join(here, "..", "lib", "web3", "addresses.ts");
writeFileSync(out, body);
console.log(`wrote ${out}`);
for (const [k, a] of entries) console.log(`  ${k.padEnd(11)} ${a}`);
